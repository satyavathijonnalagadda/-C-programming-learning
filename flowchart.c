#include<stdio.h>
int main()
{
    int a=20;
    int b=25;
    int c=260;
    int d=500;
    printf("The sum of a and b %d\n",a+b);
    scanf("%d",&a+b);
    printf("The sum of a and c %d\n",a+c);
    scanf("%d",&a+c);
    printf("The sum of a and d %d\n", a+d);
    scanf("%d",&a+d);
    printf("The sum of a,b,c and d %d\n",a+b+c+d);
    scanf("%d",&a+b+c+d);
    return 0;
}